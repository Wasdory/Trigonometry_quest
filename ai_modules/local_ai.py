    def explain_concept(self, concept):
        """Объясняет тригонометрический концепт"""
        if not concept:
            return "Пожалуйста, укажите концепт для объяснения."
        
        concept_lower = concept.lower()
        
        # Поиск точного совпадения
        for key in self.explanations:
            if key in concept_lower:
                return self.explanations[key]
        
        # Поиск частичного совпадения
        for key in self.explanations:
            if any(word in concept_lower for word in key.split()):
                return self.explanations[key]
        
        # Общее объяснение
        return ("Тригонометрия изучает соотношения между сторонами и углами треугольников. "
                "Основные тригонометрические функции: синус (sin), косинус (cos), тангенс (tan). "
                "Эти функции помогают решать задачи, связанные с углами и расстояниями.")
    
    def generate_hint(self, task_text):
        """Генерирует подсказку для задачи"""
        if not task_text:
            return "Попробуйте нарисовать схему задачи."
        
        task_lower = task_text.lower()
        
        # Поиск ключевых слов
        for key in self.hints_database:
            if key in task_lower:
                return self.hints_database[key]
        
        # Общие подсказки по категориям
        if "sin(" in task_lower or "cos(" in task_lower or "tan(" in task_lower:
            hints = [
                "Вспомните табличные значения для стандартных углов (30°, 45°, 60°, 90°)",
                "Проверьте, все ли величины в одной системе измерения (градусы/радианы)",
                "Используйте основное тригонометрическое тождество: sin²α + cos²α = 1",
                "Попробуйте нарисовать единичную окружность для наглядности"
            ]
        elif "уравнение" in task_lower:
            hints = [
                "Перенесите все члены уравнения в одну сторону",
                "Найдите значения, при которых функция равна заданному числу",
                "Вспомните период тригонометрических функций",
                "Проверьте все решения в заданном интервале"
            ]
        elif "вычислите" in task_lower or "найдите" in task_lower:
            hints = [
                "Разбейте сложное выражение на простые части",
                "Упростите выражение, используя тригонометрические тождества",
                "Подставьте известные значения",
                "Проверьте правильность вычислений по шагам"
            ]
        else:
            hints = [
                "Попробуйте нарисовать схему или график",
                "Разбейте задачу на более простые этапы",
                "Вспомните определения тригонометрических функций",
                "Проверьте, правильно ли вы поняли условие задачи"
            ]
        
        return random.choice(hints)
    
    def analyze_solution(self, user_solution, correct_solution):
        """Анализирует решение пользователя"""
        if not user_solution:
            return "Решение не предоставлено."
        
        # Простой анализ различий
        if str(user_solution).strip() == str(correct_solution).strip():
            return "Решение полностью верное! Отличная работа!"
        
        try:
            # Попытка численного сравнения
            user_num = float(user_solution)
            correct_num = float(correct_solution)
            
            error = abs(user_num - correct_num)
            if error < 0.01:
                return f"Решение почти верное (разница: {error:.4f}). Проверьте округление."
            elif error < 0.1:
                return f"Близко к правильному ответу (разница: {error:.4f}). Проверьте вычисления."
            else:
                return f"Решение неверное. Разница: {error:.4f}. Попробуйте другой подход."
                
        except ValueError:
            # Символьный анализ
            user_clean = str(user_solution).lower().replace(' ', '')
            correct_clean = str(correct_solution).lower().replace(' ', '')
            
            if user_clean == correct_clean:
                return "Решение верное! (возможно, отличается формат записи)"
            elif any(part in user_clean for part in correct_clean.split(',')):
                return "Частично верно. Проверьте, учли ли вы все возможные решения."
            else:
                return f"Решение неверное. Ожидался ответ: {correct_solution}"
    
    def test_self(self):
        """Тестирование собственной функциональности"""
        print("Тестирование AI модуля...")
        
        # Тест объяснений
        explanation = self.explain_concept("синус")
        print(f"Объяснение 'синус': {explanation[:60]}...")
        
        # Тест подсказок
        hint = self.generate_hint("Вычислите sin(30°)")
        print(f"Подсказка для sin(30°): {hint}")
        
        # Тест анализа
        analysis = self.analyze_solution("1.0", "1.0")
        print(f"Анализ решения: {analysis}")
        
        return True